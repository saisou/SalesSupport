import UIKit

extension  UIColor{
    static var SSBlue : UIColor {
        get {
            return UIColor.init(red:  42.0 / 255.0, green:  153.0 / 255.0, blue: 250.0 / 255.0, alpha: 1)
        }
    }
    static var SSGray : UIColor {
        get {
            return UIColor.init(red: 0.8667, green: 0.8667, blue: 0.8667, alpha: 1.0)
        }
    }
    static var SSlightGray : UIColor {
        get {
            return UIColor.init(red: 0.9569, green: 0.9569, blue: 0.9569, alpha: 1.0)
        }
    }
}


